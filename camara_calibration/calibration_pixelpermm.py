import cv2

# --------------------------------------
# Global Variables
# --------------------------------------


# --------------------------------------
# Functions
# --------------------------------------


def main():
    # --------------------------------------
    # Inizialitation
    # --------------------------------------
    ref_img = cv2.imread('Image_template/template.png')

    # --------------------------------------
    # Visualitazion
    # --------------------------------------
    cv2.imshow('Debug', ref_img)

    # --------------------------------------
    # Termination
    # --------------------------------------
    if cv2.waitKey(0) & 0xFF == ord('q'):
        exit(0)   

if __name__ == "__main__":
    main()